public class PhrasesTranslator 
{
    public static void main(String[] args) 
    {
        System.out.println("    English                  ->           French");
        System.out.println("------------------------------------------------------------------");
        System.out.println("Good Morning                 | Bonjour");
        System.out.println("It is a pleasure to meet you | C'est un plaisir de vous rencontrer");
        System.out.println("Please call me tomorrow      | S'il vous plait appelez-moi demain");
        System.out.println("Have a nice day!             | Bonne journee!");
        
    }
}
