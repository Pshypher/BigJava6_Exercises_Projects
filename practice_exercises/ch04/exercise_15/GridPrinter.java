/**
        This program prints a grid for the tic-tac-toe game
*/
public class GridPrinter
{
        public static void main(String[] args)
        {
                String comb = "+--+--+--+\n" + "|  |  |  |";
                String bottom = "+--+--+--+";
                
                // Create tic-tac-toe grid
                
                System.out.println(comb);
                System.out.println(comb);
                System.out.println(comb);
                System.out.println(bottom);
                                  
        }
}